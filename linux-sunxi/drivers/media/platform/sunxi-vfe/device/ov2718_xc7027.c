/*
 * On most platforms, we'd rather do straight i2c I/O.
 */
static int sensor_sunxi_read(struct v4l2_subdev *sd, addr_type reg, data_type *value)
{
	int ret=0, cnt=0;
	struct cci_driver *cci_drv = v4l2_get_subdev_hostdata(sd);
	
	ret = sensor_cci_read(sd,reg,value);
	while((ret != 0) && (cnt < 2))
	{
		ret = sensor_cci_read(sd,reg,value);
		cnt++;
	}
	if(cnt > 0)
		vfe_dev_err("%s sensor read retry=%d\n",cci_drv->name, cnt);

	return ret;
}

