/*
 * Write a list of register settings;
 */
int sensor_sunxi_write_array(struct v4l2_subdev *sd, struct regval_list *regs, int array_size)
{
	int ret = 0, i = 0;
	struct cci_driver *cci_drv = v4l2_get_subdev_hostdata(sd);

	if(!regs)
		return -EINVAL;

	while(i < array_size)
	{
		if(regs->addr == REG_DLY) {
			msleep(regs->data);
		} else {  
			ret = sensor_sunxi_write(sd, regs->addr, regs->data);
			if(ret < 0)
				printk("%s sensor write array error!!\n",cci_drv->name);
		}
		i++;
		regs++;
	}
	return 0;
}

