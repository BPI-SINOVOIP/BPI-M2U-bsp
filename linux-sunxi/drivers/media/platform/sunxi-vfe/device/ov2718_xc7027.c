static int sensor_detect(struct v4l2_subdev *sd)
{
	int ret;
	struct regval_list regs;

	regs.addr = 0xfffd;
	regs.data = 0x80;
	ret = sensor_sunxi_write(sd, regs.addr, regs.data);
	
	sensor_sunxi_write(sd, 0xfffd, 0x80);
	sensor_sunxi_write(sd, 0xfffe, 0x80);
	
	regs.addr = 0xfffe;
	regs.data = 0x80;
	ret = sensor_sunxi_write(sd, regs.addr, regs.data);
	
	regs.addr = 0xfffe;
	ret = sensor_sunxi_read(sd, regs.addr, &regs.data);

	regs.addr = 0xfffb;
	ret = sensor_sunxi_read(sd, regs.addr, &regs.data);
	if (ret < 0) {
		vfe_dev_err("sensor_sunxi_read err at sensor_detect!\n");
		return ret;
	}

	vfe_dev_dbg("ov2718_xc7027 chip  ID =%x\n ", regs.data);

	return 0;
}

static int sensor_regs_bypass_set(struct v4l2_subdev *sd, struct regval_list *regs, int array_size)
{
	int ret;
	
	/* i2c by pass on */
	ret = sensor_sunxi_write_array(sd, sensor_bypass_on, ARRAY_SIZE(sensor_bypass_on));
	if (ret < 0) {
		vfe_dev_err("write sensor_default_regs error\n");
		return ret;
	}

	/* change i2c slave to OV2718(0X6C) */
	bypass_on = 1;


	/* ov2718 detect */
	ret = ov2718_sensor_detect(sd);
	if (ret) {
		vfe_dev_err("ov2718 sensor is not an target chip.\n");
		return ret;
	}

	/* ov2718 regs init */
	ret = sensor_sunxi_write_array(sd, regs, array_size);
	if (ret < 0) {
		vfe_dev_err("write sensor_default_regs error\n");
		return ret;
	}

	/* change i2c slave to XC7027(0X36) */
	bypass_on = 0;
	
	/* i2c by pass off */
	ret = sensor_sunxi_write_array(sd, sensor_bypass_off, ARRAY_SIZE(sensor_bypass_off));
	if (ret < 0) {
		vfe_dev_err("write sensor_default_regs error\n");
		return ret;
	}

	return 0;
}

