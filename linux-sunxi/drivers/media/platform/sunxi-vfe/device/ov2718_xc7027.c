static int sensor_cci_write_a16_d8(struct v4l2_subdev *sd, unsigned short addr,
    unsigned char value)
{
	int ret = 0;
	struct i2c_msg msg;
	unsigned char data[3];
	struct i2c_client *client = v4l2_get_subdevdata(sd);

	if(bypass_on == 1)
		client->addr = OV2718_WRITE_ADDR >> 1;
	else if(bypass_on == 0)
		client->addr = XC7027_WRITE_ADDR >> 1;
	else
		vfe_dev_err("%s incorrect flag, can't switch slave addr(bypass_on = %d)", __func__, bypass_on);

	data[0] = (addr&0xff00)>>8;
	data[1] = (addr&0x00ff);
	data[2] = value;

	msg.addr = client->addr;
	msg.flags = 0;
	msg.len = 3;
	msg.buf = data;

	ret = i2c_transfer(client->adapter, &msg, 1);
	if (ret >= 0) {
	ret = 0;
	} 
	else 
	{
		vfe_dev_err("%s error! slave = 0x%x, addr = 0x%4x, value = 0x%4x\n ",__func__, client->addr, addr,value);
	}
	
	return ret;
}

static int sensor_cci_read_a16_d8(struct v4l2_subdev *sd, unsigned short addr,
    unsigned char *value)
{ 
	int ret;
	unsigned char data[3];
	struct i2c_msg msg[2];
	struct i2c_client *client = v4l2_get_subdevdata(sd);

	if(bypass_on == 1)
		client->addr = OV2718_WRITE_ADDR >> 1;
	else if(bypass_on == 0)
		client->addr = XC7027_WRITE_ADDR >> 1;
	else
		vfe_dev_err("%s incorrect flag, can't switch slave addr(bypass_on = %d)", __func__, bypass_on);

  	data[0] = (addr&0xff00)>>8;
	data[1] = (addr&0x00ff);
	data[2] = 0xee;
  
	/*
 	 * Send out the register address...
 	 */ 
	msg[0].addr = client->addr;
	msg[0].flags = 0;
	msg[0].len = 2;
	msg[0].buf = &data[0];
  
	/*
	 * ...then read back the result.
	 */
	msg[1].addr = client->addr;
	msg[1].flags = I2C_M_RD;
	msg[1].len = 1;
	msg[1].buf = &data[2];
  
	ret = i2c_transfer(client->adapter, msg, 2);
	if (ret >= 0) {
 		*value = data[2];
 	ret = 0;
  	} 
  	else 
  	{
    	vfe_dev_err("%s error! slave = 0x%x, addr = 0x%4x, value = 0x%2x\n ",__func__, client->addr, addr,*value);
  	}  
  
  	return ret;
}

static int sensor_cci_write(struct v4l2_subdev *sd, addr_type addr, data_type value)
{
	struct cci_driver *cci_drv = v4l2_get_subdev_hostdata(sd);
	int addr_width = cci_drv->addr_width;
	int data_width = cci_drv->data_width;

	if (16 == addr_width && 8 == data_width)
	{
		return sensor_cci_write_a16_d8(sd, addr, (unsigned char)value);
	}
	else
	{
		vfe_dev_err("%s error! addr_width = %d , data_width = %d!\n ",__func__, addr_width, data_width);
		return -1;
	}
}

